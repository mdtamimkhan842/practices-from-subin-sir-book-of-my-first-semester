#include <stdio.h>
#include <math.h>
int main ()
{
    int A,B, sum, division, multiply, subtraction;
    printf ("");
    scanf ("%d%d ", &A, &B);
    printf ("%d+%d=%d\n",A,B,sum);
    printf ("%d-%d=%d\n", A,B,subtraction);
    printf ("%d*%d=%d\n", A,B,multiply);
    printf ("%d/%d=%d\n", A,B,division);
    return 0;
}
